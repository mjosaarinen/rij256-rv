//  === Simple implemenation of AES-256 with RISC-V Vector Crypto Intrinsics

#include <stdio.h>
#include <string.h>
#include "aes256_rv.h"

//  === Check AES-256 test vectors

int aes256_test()
{
    //  from https://csrc.nist.gov/CSRC/media/Projects/Cryptographic-Standards-and-Guidelines/documents/examples/AES_Core256.pdf

    //  AES-256 key
    const uint8_t tv_sk[32] = {
        0x60, 0x3D, 0xEB, 0x10, 0x15, 0xCA, 0x71, 0xBE,
        0x2B, 0x73, 0xAE, 0xF0, 0x85, 0x7D, 0x77, 0x81,
        0x1F, 0x35, 0x2C, 0x07, 0x3B, 0x61, 0x08, 0xD7,
        0x2D, 0x98, 0x10, 0xA3, 0x09, 0x14, 0xDF, 0xF4
    };

    //  Four 128-bit blocks of AES-256 plaintext and ciphertext
    const uint8_t tv_pt[64] = {
        0x6B, 0xC1, 0xBE, 0xE2, 0x2E, 0x40, 0x9F, 0x96,
        0xE9, 0x3D, 0x7E, 0x11, 0x73, 0x93, 0x17, 0x2A,
        0xAE, 0x2D, 0x8A, 0x57, 0x1E, 0x03, 0xAC, 0x9C,
        0x9E, 0xB7, 0x6F, 0xAC, 0x45, 0xAF, 0x8E, 0x51,
        0x30, 0xC8, 0x1C, 0x46, 0xA3, 0x5C, 0xE4, 0x11,
        0xE5, 0xFB, 0xC1, 0x19, 0x1A, 0x0A, 0x52, 0xEF,
        0xF6, 0x9F, 0x24, 0x45, 0xDF, 0x4F, 0x9B, 0x17,
        0xAD, 0x2B, 0x41, 0x7B, 0xE6, 0x6C, 0x37, 0x10
    };

    const uint8_t tv_ct[64] = {
        0xF3, 0xEE, 0xD1, 0xBD, 0xB5, 0xD2, 0xA0, 0x3C,
        0x06, 0x4B, 0x5A, 0x7E, 0x3D, 0xB1, 0x81, 0xF8,
        0x59, 0x1C, 0xCB, 0x10, 0xD4, 0x10, 0xED, 0x26,
        0xDC, 0x5B, 0xA7, 0x4A, 0x31, 0x36, 0x28, 0x70,
        0xB6, 0xED, 0x21, 0xB9, 0x9C, 0xA6, 0xF4, 0xF9,
        0xF1, 0x53, 0xE7, 0xB1, 0xBE, 0xAF, 0xED, 0x1D,
        0x23, 0x30, 0x4B, 0x7A, 0x39, 0xF9, 0xF3, 0xFF,
        0x06, 0x7D, 0x8D, 0x8F, 0x9E, 0x24, 0xEC, 0xC7
    };

    int fail = 0;
    uint8_t xt[64] = { 0 };
    uint32_t rk[15 * 4] = { 0 };

    //  expand our key
    memset(rk, 0x11, sizeof(rk));
    aes256_exp_key(rk, tv_sk);

    //  Encrypt
    memset(xt, 0x22, sizeof(xt));
    aes256_enc(xt, tv_pt, 64, rk);
    if (memcmp(xt, tv_ct, 64) != 0) {
        printf("[FAIL] aes256_test(): encrypt\n");
        fail++;
    }

    //  Decrypt
    memset(xt, 0x33, sizeof(xt));
    aes256_dec(xt, tv_ct, 64, rk);
    if (memcmp(xt, tv_pt, 64) != 0) {
        printf("[FAIL] aes256_test(): decrypt\n");
        fail++;
    }

    return fail;
}

