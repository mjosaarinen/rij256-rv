//  === Some self-tests for Rijndael-(256,256)

#include <stdio.h>
#include <string.h>

#include "plat_local.h"
#include "rij256_rv.h"

int rij256_test()
{
    //  Test vectors in "B.3 Other Block Lengths and Key Lengths" (Page 257)
    //  J. Daemen & V. Rijmen: "The Design of Rijndael" (2nd Edition), 2020.
    //  (Zero key; tv0 is ct from encrypting zeros, tv1 from encrypting tv0.)
    const uint8_t tv0_b3[32] = {
        0xC6, 0x22, 0x7E, 0x77, 0x40, 0xB7, 0xE5, 0x3B,
        0x5C, 0xB7, 0x78, 0x65, 0x27, 0x8E, 0xAB, 0x07,
        0x26, 0xF6, 0x23, 0x66, 0xD9, 0xAA, 0xBA, 0xD9,
        0x08, 0x93, 0x61, 0x23, 0xA1, 0xFC, 0x8A, 0xF3
    };

    const uint8_t tv1_b3[32] = {
        0x98, 0x43, 0xE8, 0x07, 0x31, 0x9C, 0x32, 0xAD,
        0x1E, 0xA3, 0x93, 0x5E, 0xF5, 0x6A, 0x2B, 0xA9,
        0x6E, 0x4B, 0xF1, 0x9C, 0x30, 0xE4, 0x7D, 0x88,
        0xA2, 0xB9, 0x7C, 0xBB, 0xF2, 0xE1, 0x59, 0xE7
    };

    //  same secret key as in AES-256 test; we just expand 2x size
    const uint8_t tv2_sk[32] = {
        0x60, 0x3D, 0xEB, 0x10, 0x15, 0xCA, 0x71, 0xBE,
        0x2B, 0x73, 0xAE, 0xF0, 0x85, 0x7D, 0x77, 0x81,
        0x1F, 0x35, 0x2C, 0x07, 0x3B, 0x61, 0x08, 0xD7,
        0x2D, 0x98, 0x10, 0xA3, 0x09, 0x14, 0xDF, 0xF4
    };

    //  Two blocks of plaintext and ciphertext.
    const uint8_t tv2_pt[64] = {
        0x6B, 0xC1, 0xBE, 0xE2, 0x2E, 0x40, 0x9F, 0x96,
        0xE9, 0x3D, 0x7E, 0x11, 0x73, 0x93, 0x17, 0x2A,
        0xAE, 0x2D, 0x8A, 0x57, 0x1E, 0x03, 0xAC, 0x9C,
        0x9E, 0xB7, 0x6F, 0xAC, 0x45, 0xAF, 0x8E, 0x51,
        0x30, 0xC8, 0x1C, 0x46, 0xA3, 0x5C, 0xE4, 0x11,
        0xE5, 0xFB, 0xC1, 0x19, 0x1A, 0x0A, 0x52, 0xEF,
        0xF6, 0x9F, 0x24, 0x45, 0xDF, 0x4F, 0x9B, 0x17,
        0xAD, 0x2B, 0x41, 0x7B, 0xE6, 0x6C, 0x37, 0x10
    };

    //  Test vector created with the Rijndael reference code from 2001.
    const uint8_t tv2_ct[64] = {
        0x66, 0xA8, 0xC1, 0x9A, 0x07, 0x24, 0x3A, 0x7C,
        0xCE, 0xD7, 0xAD, 0xED, 0x37, 0xE6, 0x65, 0x1F,
        0x48, 0x5E, 0x9F, 0x79, 0xD2, 0x93, 0xC4, 0x39,
        0x97, 0x7D, 0x90, 0xD4, 0x5A, 0x7D, 0xC0, 0x29,
        0x4B, 0x5F, 0xA2, 0xF3, 0xC2, 0xCA, 0x20, 0xAA,
        0xEC, 0xB9, 0x12, 0xEB, 0x11, 0x6C, 0x09, 0xBF,
        0x9B, 0x7D, 0x10, 0x9C, 0xE6, 0x9E, 0xA3, 0x9A,
        0x67, 0xBE, 0x9B, 0xB1, 0xA3, 0xB0, 0xD4, 0x15
    };

    int fail = 0;
    uint8_t t0[32] = { 0 };
    uint8_t xt[64] = { 0 };
    uint32_t rk[15 * 8] = { 0 };

    memset(t0, 0x00, sizeof(t0));           //  zero vector

    memset(rk, 0x11, sizeof(rk));           //  set static pattern
    rij256_exp_key(rk, t0);                 //  expand zero key

    memset(xt, 0x22, sizeof(xt));
    rij256_enc(xt, t0, 32, rk);             //  encrypt zeros with zero key
    if (memcmp(xt, tv0_b3, 32) != 0) {
        printf("[FAIL] rij256_test(): encrypt #0\n");
        fail++;
    }
    memset(xt, 0x33, sizeof(xt));
    rij256_dec(xt, tv0_b3, 32, rk);         //  decrypt
    if (memcmp(xt, t0, 32) != 0) {
        printf("[FAIL] rij256_test(): decrypt #0\n");
        fail++;
    }

    memset(xt, 0x44, sizeof(xt));
    rij256_enc(xt, tv0_b3, 32, rk);         //  encrypt tv0 with zero key
    if (memcmp(xt, tv1_b3, 32) != 0) {
        printf("[FAIL] rij256_test(): encrypt #1\n");
        fail++;
    }
    memset(xt, 0x55, sizeof(xt));
    rij256_dec(xt, tv1_b3, 32, rk);         //  decrypt
    if (memcmp(xt, tv0_b3, 32) != 0) {
        printf("[FAIL] rij256_test(): decrypt #1\n");
        fail++;
    }

    memset(rk, 0x66, sizeof(rk));
    rij256_exp_key(rk, tv2_sk);             //  secomd test vectors

    memset(xt, 0x77, sizeof(xt));
    rij256_enc(xt, tv2_pt, 64, rk);
    if (memcmp(xt, tv2_ct, 64) != 0) {
        printf("[FAIL] rij256_test(): encrypt #2\n");
        fail++;
    }

    memset(xt, 0x88, sizeof(xt));
    rij256_dec(xt, tv2_ct, 64, rk);
    if (memcmp(xt, tv2_pt, 64) != 0) {
        printf("[FAIL] rij256_test(): decrypt #2\n");
        fail++;
    }

    return fail;
}

